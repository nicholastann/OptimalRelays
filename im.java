public class im {
   private double back;
   private double breast;
   private double fly;
   private double free;
 
   public im (double back, double breast, double fly, double free) {
       this.back = back;
       this.breast = breast;
       this.fly = fly;
       this.free = free;
   }
 
   public double getback() {
       return back;
   }
 
   public double getbreast() {
       return breast;
   }
 
   public double getfly() {
       return fly;
   }
 
   public double getfree() {
       return free;
   }
 
}